public class AddressPrint {
    // This program prints a person's address information to the console
    // including first name, last name, street address, city, and zip code.

    public static void main(String[] args) {
        String firstname = "Damien";
        String lastname = "Andrews";
        String streetAddress = "8675 Main St";
        String city = "Random Lake";
        String zipCode = "53075";

        System.out.println(firstname);
        System.out.println(lastname);
        System.out.println(streetAddress);
        System.out.println(city);
        System.out.println(zipCode);
    }
}
